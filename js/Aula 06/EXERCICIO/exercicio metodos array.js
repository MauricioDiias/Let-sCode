// METODO CONCAT

var front = ["html,css,javascript"];
let back = ["java", "php", "node"];

console.log(front.concat(back));

//METODO JOIN

console.log(front.join("-"));

// METODO PUSH

console.log(front.push("python"));

// METODO POP

console.log(front.pop(front));

// METODO SHIFT
console.log(front.shift(front));

//METODO REVERSE
console.log(front.reverse(front));
